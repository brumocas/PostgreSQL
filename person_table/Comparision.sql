-- SQL Comparison Operators --

-- Equal to (=): Used to compare if two expressions are equal.
-- Example: SELECT * FROM table_name WHERE column_name = value;

-- Not equal to (<> or !=): Used to compare if two expressions are not equal.
-- Example: SELECT * FROM table_name WHERE column_name <> value;
--          SELECT * FROM table_name WHERE column_name != value;

-- Greater than (>): Used to compare if one expression is greater than another.
-- Example: SELECT * FROM table_name WHERE column_name > value;

-- Greater than or equal to (>=): Used to compare if one expression is greater than or equal to another.
-- Example: SELECT * FROM table_name WHERE column_name >= value;

-- Less than (<): Used to compare if one expression is less than another.
-- Example: SELECT * FROM table_name WHERE column_name < value;

-- Less than or equal to (<=): Used to compare if one expression is less than or equal to another.
-- Example: SELECT * FROM table_name WHERE column_name <= value;

-- Between: Used to specify a range of values.
-- Example: SELECT * FROM table_name WHERE column_name BETWEEN value1 AND value2;

-- IN: Used to specify multiple possible values for a column.
-- Example: SELECT * FROM table_name WHERE column_name IN (value1, value2, ...);

-- NOT IN: Used to specify values that should not be included.
-- Example: SELECT * FROM table_name WHERE column_name NOT IN (value1, value2, ...);

-- LIKE: Used to search for a specified pattern in a column.
-- Example: SELECT * FROM table_name WHERE column_name LIKE pattern;

-- IS NULL: Used to check if a value is NULL.
-- Example: SELECT * FROM table_name WHERE column_name IS NULL;

-- IS NOT NULL: Used to check if a value is not NULL.
-- Example: SELECT * FROM table_name WHERE column_name IS NOT NULL;
